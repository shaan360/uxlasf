        //\Yii::$app->response->format = \yii\web\Response::FORMAT_JSON;
     $out = ['results' => ['id' => '', 'text' => '']];
        if (!is_null($q)) {
            $query = new \yii\db\Query;
            $query->select('id, concat([[unit_type]]) AS text')
                    ->from('units')
                    ->where(['like', 'unit_type', $q])
